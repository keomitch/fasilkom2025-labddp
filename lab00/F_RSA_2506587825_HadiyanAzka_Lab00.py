t.color("blue")
t.forward(30)
t.right(90)
t.forward(50)
t.left(90)
t.forward(30)
t.left(90)
t.forward(50)
t.right(90)
t.forward(30)
t.right(90)
t.forward(130)
t.right(90)
t.forward(30)
t.right(90)
t.forward(50)
t.left(90)
t.forward(30)
t.left(90)
t.forward(50)
t.right(90)
t.forward(30)
t.right(90)
t.forward(130)
t.penup()

# space
t.goto(50, 150)

# writing letter A
t.pendown()
t.color("red")
t.right(160)
t.forward(140)
t.right(110)
t.forward(30)
